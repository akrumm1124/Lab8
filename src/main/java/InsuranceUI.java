    private void calculateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateButtonActionPerformed

        try {
            int age = Integer.parseInt(ageInput.getText());
            int insurancePrice = calculateInsurance(age);
            resultsLabel.setText("Since you are " + age + " years old, your insurance will cost $" + insurancePrice + " per year.");
        }
        catch (InvalidAgeException ex) {
            JOptionPane.showMessageDialog(this, "Invalid age. Age must be between 5 and 110.");
        }
        catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Please enter a valid number.");
        }
        catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "There was an error.");
        }
    }//GEN-LAST:event_calculateButtonActionPerformed

