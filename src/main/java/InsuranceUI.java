    public static int calculateInsurance (int age) throws InvalidAgeException {
        if (age < 5 || age > 110) {
            throw new InvalidAgeException("Invalid age.");
        }    
        int insurancePrice = (age * 5) + 300;
       return insurancePrice;
    }
    
